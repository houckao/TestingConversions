"""convert miles per gallon to unit of distance for fuel"""
def convertMPGtoKPL(mpg):
    return mpg*.425144

""""converting miles to kliometers"""
def convertMItoKM(mi):
    return mi*1.60934

"""converting gallons to liters"""
def convertGALtoL(gal):
    return gal*3.78541

"""converting centimeters to inches"""
def convertCMtoIN(cm):
    return cm*0.393701

"""converting feet to miles"""
def convertFTtoMI(ft):
    return ft*0.000189394
